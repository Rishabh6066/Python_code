#Arithmatic operators are used to perform mathematical operations on numerical values.
a=10
b=5

#1-Addition
add= a+b
print(add)

#2-Subtraction
sub= a-b
print(sub)

#3-Multiplication
mul= a*b
print(mul)

#4-Division
div= a/b
print(div)

#5-Modulus
mod= a%b
print(mod)

#6-Floor Division
f_div= a+b
print(f_div)

#7-Exponential
expo=a**b
print(expo)